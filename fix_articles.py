# Process each article
for article_num in range(1, 11):
    filepath = os.path.join(BASE, f"article-{article_num}.html")
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Extract article-body content
    body_match = re.search(r'(<div class="article-body">)(.*?)(</div>\s*<div class="cta)', content, re.DOTALL)
    if not body_match:
        print(f"article-{article_num}: Could not find article-body")
        continue
    
    body_content = body_match.group(2)
    
    # Apply expansions
    article_expansions = expansions[article_num]
    for h2_title, new_elements in article_expansions.items():
        # Find the H2 and insert content after the H2's following content
        # We'll insert after the last element before the next H2 or before the next H2
        # Strategy: find the H2, then find the next H2 or end of content, insert before next H2
        
        # Find H2 position
        h2_pattern = f'<h2>{re.escape(h2_title)}</h2>'
        h2_match = re.search(h2_pattern, body_content)
        if not h2_match:
            print(f"article-{article_num}: H2 '{h2_title}' not found")
            continue
        
        h2_end = h2_match.end()
        
        # Find next H2 or end of body
        next_h2 = re.search(r'<h2>', body_content[h2_end:])
        
        if next_h2:
            insert_pos = h2_end + next_h2.start()
        else:
            insert_pos = len(body_content)
        
        # Build insertion text
        insert_text = '\n'.join(new_elements)
        
        body_content = body_content[:insert_pos] + '\n' + insert_text + '\n' + body_content[insert_pos:]
    
    # Reconstruct full HTML
    new_content = content[:body_match.start(2)] + body_content + content[body_match.end(2):]
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"article-{article_num}: Updated")

